public class Costa implements Objetivo{
	
	// Atributos
	
	private int poblacion;
	private int monedas;
		
	// Getters y Setters
	
	public int getPoblacion(){
		return poblacion;
	}
	
	public void setPoblacion(int Poblacion){
		poblacion = Poblacion;
	}
	
	public int getMonedas(){
		return monedas;
	}
	
	public void setMonedas(int Monedas){
		monedas = Monedas;
	}
}
